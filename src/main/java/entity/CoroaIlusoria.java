package entity;

public class CoroaIlusoria {
    private Boolean perdida;
    private String tipoDeIlusao;

    public Boolean getPerdida() {
        return perdida;
    }

    public void setPerdida(Boolean perdida) {
        this.perdida = perdida;
    }

    public String getTipoDeIlusao() {
        return tipoDeIlusao;
    }

    public void setTipoDeIlusao(String tipoDeIlusao) {
        this.tipoDeIlusao = tipoDeIlusao;
    }
}
